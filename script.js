// Preload images for better performance
function preloadImages() {
    const images = [
        'snake.jpg',
        'https://via.placeholder.com/300x200/4f46e5/ffffff?text=E-Commerce',
        'https://via.placeholder.com/300x200/10b981/ffffff?text=Task+Manager',
        'https://via.placeholder.com/300x200/f59e0b/ffffff?text=Weather+App'
    ];
    
    images.forEach(src => {
        const img = new Image();
        img.src = src;
    });
}

// Initialize preloading on page load
document.addEventListener('DOMContentLoaded', preloadImages);

// Performance optimization: Lazy loading for project images
if ('IntersectionObserver' in window) {
    const imageObserver = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const img = entry.target;
                img.src = img.dataset.src;
                img.classList.remove('lazy');
                imageObserver.unobserve(img);
            }
        });
    });

    document.querySelectorAll('img[data-src]').forEach(img => {
        imageObserver.observe(img);
    });
}